public class _021DoWhile {
    public static void main(String[] args) {
        int a = 6;
        do{ // Execute at least one time
            System.out.println(a);
            a++;
        }while(a<5);

        //Quick quiz
        int b =1;
        do {
            System.out.println(b);
            b++;
        } while (b<=50);
    }
}
